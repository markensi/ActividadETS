package dam_ed04_actividad;

/**
 *
 * @author Marcos
 */
public class Banquero {
    String nombre;
    String apellido;
    int codBanquero;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public int getCodBanquero() {
        return codBanquero;
    }

    public void setCodBanquero(int codBanquero) {
        this.codBanquero = codBanquero;
    }
    
}
